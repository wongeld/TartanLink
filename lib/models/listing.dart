class Listing {
  final String id;
  final String title;
  final String description;
  final String city;
  final double price;
  final int rooms;
  final String image;
  final String ownerId;

  Listing({
    required this.id,
    required this.title,
    required this.description,
    required this.city,
    required this.price,
    required this.rooms,
    required this.image,
    required this.ownerId,
  });
}
